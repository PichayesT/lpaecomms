    <p>To manage stock, navigate to the <strong>Stock Management</strong> page. Here, you can add, update, or delete stock items. Use the search bar to quickly find specific items.</p>

    <h2 id="section2">2. Sales and Invoicing</h2>
    <p>On the <strong>Sales and Invoicing</strong> page, you can create new invoices, view existing invoices, and delete invoices. Use the "Check Detail" button to view detailed information about an invoice.</p>

    <h2 id="section3">3. Clients Management</h2>
    <p>The <strong>Clients Management</strong> page allows you to add, update, or delete client information. You can also search for clients using the search bar.</p>

    <h2 id="section4">4. System Administration</h2>
    <p>System administrators can manage user accounts and permissions on the <strong>System Administration</strong> page. Ensure only authorized personnel have access to this section.</p>

    <h2 id="section5">5. Theme Customization</h2>
    <p>You can switch between light and dark themes using the theme icons in the top-right corner of the page. Click the sun icon for light mode and the moon icon for dark mode.</p>

